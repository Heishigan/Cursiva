def _get_app_root_dir(company_name, role_name):
    import re
    def sanitize(text):
        text = text.replace(" ", "_")
        return re.sub(r'[\\/*?:"<>|]', "", text)
    c_name = sanitize(company_name)
    r_name = sanitize(role_name)
    return os.path.join(root_dir, "Applications", f"{c_name}_{r_name}"), c_name

def compile_cv_pdf(cv_data, company_name, role_name):
    app_root_dir, c_name = _get_app_root_dir(company_name, role_name)
    template_dir = os.path.join(engine_dir, 'templates')
    jinja_env = jinja2.Environment(loader=jinja2.FileSystemLoader(template_dir))

    def latex_escape(text):
        if not text: return ""
        text = str(text).replace('\\', r'\textbackslash{}')
        escape_chars = {
            '&': r'\&', '%': r'\%', '$': r'\$', '#': r'\#', '_': r'\_',
            '{': r'\{', '}': r'\}', '~': r'\textasciitilde{}',
            '^': r'\textasciicircum{}'
        }
        text = "".join(escape_chars.get(c, c) for c in text)
        return text.replace('\n', r'\\ ')

    def escape_data(obj):
        if isinstance(obj, str):
            return latex_escape(obj)
        elif isinstance(obj, dict):
            return {k: escape_data(v) for k, v in obj.items()}
        elif isinstance(obj, list):
            return [escape_data(i) for i in obj]
        return obj

    safe_data = escape_data(cv_data)
    personal_info = safe_data.get("personal_info", {})
    
    # Render CV
    template_cv = jinja_env.get_template('cv_template.tex')
    
    rendered_cv = template_cv.render(
        personal_info=personal_info,
        professional_summary=safe_data.get("professional_summary", ""),
        sections=safe_data.get("sections", [])
    )
    latex_dir = os.path.join(app_root_dir, "latex", "cv")
    os.makedirs(latex_dir, exist_ok=True)
    tex_path = os.path.join(latex_dir, f"{c_name}_CV.tex")
    with open(tex_path, "w", encoding="utf-8") as f:
        f.write(rendered_cv)
        
    try:
        subprocess.run(['pdflatex', '-interaction=nonstopmode', f"{c_name}_CV.tex"], cwd=latex_dir, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=True)
        compiled_pdf = os.path.join(latex_dir, f"{c_name}_CV.pdf")
        if os.path.exists(compiled_pdf):
            return compiled_pdf
    except subprocess.CalledProcessError:
        pass
    return None

def compile_cl_pdf(cl_paragraphs, personal_info, company_name, role_name):
    if not cl_paragraphs:
        return None
        
    app_root_dir, c_name = _get_app_root_dir(company_name, role_name)
    template_dir = os.path.join(engine_dir, 'templates')
    jinja_env = jinja2.Environment(loader=jinja2.FileSystemLoader(template_dir))
    
    def latex_escape(text):
        if not text: return ""
        text = str(text).replace('\\', r'\textbackslash{}')
        escape_chars = {
            '&': r'\&', '%': r'\%', '$': r'\$', '#': r'\#', '_': r'\_',
            '{': r'\{', '}': r'\}', '~': r'\textasciitilde{}',
            '^': r'\textasciicircum{}'
        }
        text = "".join(escape_chars.get(c, c) for c in text)
        return text.replace('\n', r'\\ ')
        
    template_cl = jinja_env.get_template('cl_template.tex')
    safe_paragraphs = [latex_escape(p) for p in cl_paragraphs]
    rendered_cl = template_cl.render(
        personal_info=personal_info,
        company_name=latex_escape(company_name),
        cover_letter_paragraphs=safe_paragraphs
    )
    
    latex_cl_dir = os.path.join(app_root_dir, "latex", "cover_letter")
    os.makedirs(latex_cl_dir, exist_ok=True)
    tex_cl_path = os.path.join(latex_cl_dir, f"{c_name}_CL.tex")
    with open(tex_cl_path, "w", encoding="utf-8") as f:
        f.write(rendered_cl)
        
    cl_template_root = os.path.join(root_dir, 'Generic_Cover_Letter_Template')
    cover_cls_src = os.path.join(cl_template_root, 'cover.cls')
    openfonts_src = os.path.join(cl_template_root, 'OpenFonts')
    if os.path.exists(cover_cls_src):
        shutil.copy2(cover_cls_src, os.path.join(latex_cl_dir, 'cover.cls'))
    if os.path.exists(openfonts_src):
        dest_fonts = os.path.join(latex_cl_dir, 'OpenFonts')
        if not os.path.exists(dest_fonts):
            shutil.copytree(openfonts_src, dest_fonts)
            
    try:
        subprocess.run(['xelatex', '-interaction=nonstopmode', f"{c_name}_CL.tex"], cwd=latex_cl_dir, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=True)
        compiled_cl = os.path.join(latex_cl_dir, f"{c_name}_CL.pdf")
        if os.path.exists(compiled_cl):
            return compiled_cl
    except subprocess.CalledProcessError:
        pass
    return None

def compiler_node(state: AgentState):
    tailored_cv = state.get("tailored_cv", {})
    app_root_dir, c_name = _get_app_root_dir(state["company_name"], state["role_name"])
    
    compiled_pdf = compile_cv_pdf(tailored_cv, state["company_name"], state["role_name"])
    if compiled_pdf:
        final_pdf_path = os.path.join(app_root_dir, f"Heishigan_Pathmaraj_CV_{c_name}.pdf")
        shutil.copy2(compiled_pdf, final_pdf_path)

    # Render Cover Letter
    personal_info = tailored_cv.get("personal_info", {})
    cl_paragraphs = state.get("cover_letter_paragraphs", [])
    if state.get("generate_cover_letter") and len(cl_paragraphs) > 0 and not state.get("cl_safety_failed", False):
        compiled_cl = compile_cl_pdf(cl_paragraphs, personal_info, state["company_name"], state["role_name"])
        if compiled_cl:
            final_cl_path = os.path.join(app_root_dir, f"Heishigan_Pathmaraj_Cover_Letter_{c_name}.pdf")
            shutil.move(compiled_cl, final_cl_path)
            
    return {}

def review_conditional(state: AgentState):
    if state["review_feedback"] == "PASS" or state["revision_count"] >= 3:
        return "pass"
    return "fail"



# --- BUILD GRAPH ---
# setup_node and strategist_node are called directly from gui.py as plain functions.
# Graph: tailor → reviewer → cover_letter → compiler
# Note: cl_reviewer removed (LLM critic was unreliable). Human review at the compiler
# interrupt is the quality gate for the cover letter.
memory = MemorySaver()
builder = StateGraph(AgentState)

builder.add_node("tailor", tailor_node)
