builder.add_node("compiler", compiler_node)

builder.set_entry_point("tailor")
builder.add_edge("tailor", "reviewer")
builder.add_conditional_edges("reviewer", review_conditional, {
    "pass": "cover_letter",
    "fail": "tailor"
})
builder.add_edge("cover_letter", "compiler")
builder.add_edge("compiler", END)

# Pause before compiler for human review only.
app = builder.compile(checkpointer=memory, interrupt_before=["compiler"])
